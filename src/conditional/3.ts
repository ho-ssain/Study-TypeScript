// The switch statement allows you to perform different actions based on the value of an expression.

const dayOfWeek: number = 3;

switch (dayOfWeek) {
  case 1:
    console.log("Monday");
    break;
  case 2:
    console.log("Tuesday");
    break;
  case 3:
    console.log("Wednesday");
    break;
  default:
    console.log("Other day");
    break;
}
